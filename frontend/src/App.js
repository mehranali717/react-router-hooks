import { RouterProvider, createBrowserRouter } from 'react-router-dom';
import { action as manipulateEventAction } from './components/EventForm';
import { EditEventPage, ErrorPage, EventDetailPage, EventsPage, EventsRootLayout, HomePage, NewEventPage, NewsletterPage, RootLayout } from './pages';
import { action as deleteEventAction, loader as eventDetailLoader } from './pages/EventDetail';
import { loader as eventsLoader } from './pages/Events';
import { action as newsletterAction } from './pages/Newsletter';

const router = createBrowserRouter([
  {
    path: '/',
    element: <RootLayout />,
    errorElement: <ErrorPage />,
    children: [
      { index: true, element: <HomePage /> },
      {
        path: 'events',
        element: <EventsRootLayout />,
        children: [
          {
            index: true,
            element: <EventsPage />,
            loader: eventsLoader,
          },
          {
            path: ':eventId',
            id: 'event-detail',
            loader: eventDetailLoader,
            children: [
              {
                index: true,
                element: <EventDetailPage />,
                action: deleteEventAction,
              },
              {
                path: 'edit',
                element: <EditEventPage />,
                action: manipulateEventAction,
              },
            ],
          },
          {
            path: 'new',
            element: <NewEventPage />,
            action: manipulateEventAction,
          },
        ],
      },
      {
        path: 'newsletter',
        element: <NewsletterPage />,
        action: newsletterAction,
      },
    ],
  },
]);

function App() {
  return <RouterProvider router={router} />;
}

export default App;